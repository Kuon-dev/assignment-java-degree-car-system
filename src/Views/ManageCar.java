  public void updateTableInformation(ArrayList<GeneralCar> data) {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    for (GeneralCar car : data) {
      Object[] eachCar = {
        car.getCarNoPlate(),
        car.getBrand(),
        car.getModel(),
        car.getYear(),
        car.getFuelType(),
        car.getPrice(),
        car.getState(),
      };

      model.addRow(eachCar);
    }
  }

  private Boolean sanitizeInput() {
    Validator v = new Validator();
    ArrayList<String> inputData = new ArrayList<>();
    inputData.add(CarNoPlate.getText()); // plate
    inputData.add(Brand.getText()); // brand
    inputData.add(Model.getText()); // model
    inputData.add(Status.getSelectedItem().toString().toUpperCase()); // status
    inputData.add(Year.getText()); // year
    inputData.add(Price.getText()); // price
    inputData.add(FuelType.getSelectedItem().toString()); // fuel

    for (int i = 0; i < inputData.size(); i++) {
      if (inputData.get(i).isEmpty()) {
        JOptionPane.showMessageDialog(
          this,
          "Please fill in all the data before submitting",
          "Error Message",
          JOptionPane.ERROR_MESSAGE
        );
        return false;
      }
    }
    if (!v.isNumber(inputData.get(4)) || !v.isNumber(inputData.get(5))) {
      JOptionPane.showMessageDialog(
        this,
        "Please input a valid number",
        "Error Message",
        JOptionPane.ERROR_MESSAGE
      );
      return false;
    }

    return true;
  }

