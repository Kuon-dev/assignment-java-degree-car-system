  private void viewcarActionPerformed(java.awt.event.ActionEvent evt) {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    GeneralGetters g = new GeneralGetters();
    ArrayList<GeneralCar> allCars = g.getAllCar();

    for (GeneralCar car : allCars) {
      Object[] eachCar = {
        car.getCarNoPlate(),
        car.getBrand(),
        car.getModel(),
        car.getYear(),
        car.getFuelType(),
        car.getPrice(),
        car.getState(),
      };

      model.addRow(eachCar);
    }
  }

  private void updateActionPerformed(java.awt.event.ActionEvent evt) {
    if (!sanitizeInput()) return;
    if (tableSelectedCar.getCarNoPlate() == null) {
      JOptionPane.showMessageDialog(
        this,
        "Please select a car to edit",
        "Error Message",
        JOptionPane.ERROR_MESSAGE
      );
    }
    GeneralMutation m = new GeneralMutation();
    GeneralCar newCar = new GeneralCar(
      CarNoPlate.getText(), // plate
      Brand.getText(), // brand
      Model.getText(), // model
      Status.getSelectedItem().toString().toUpperCase(), // status
      Integer.parseInt(Year.getText()), // year
      Double.parseDouble(Price.getText()), // price
      FuelType.getSelectedItem().toString() // fuel
    );

    if (
      m.editExistingCar(tableSelectedCar, newCar)
    ) JOptionPane.showMessageDialog(
      this,
      "Record Added Successfully",
      "Information",
      JOptionPane.INFORMATION_MESSAGE
    ); else JOptionPane.showMessageDialog(
      this,
      "Failed to modify car",
      "Error Message",
      JOptionPane.ERROR_MESSAGE
    );
  }

  private void deleteActionPerformed(java.awt.event.ActionEvent evt) {
    if (tableSelectedCar.getCarNoPlate() == null) {
      JOptionPane.showMessageDialog(
        this,
        "Please select a car to delete",
        "Error Message",
        JOptionPane.ERROR_MESSAGE
      );
      return;
    }
    GeneralMutation m = new GeneralMutation();
    if (m.deleteExistingCar(tableSelectedCar)) JOptionPane.showMessageDialog(
      this,
      "Record Deleted Successfully",
      "Information",
      JOptionPane.INFORMATION_MESSAGE
    ); else JOptionPane.showMessageDialog(
      this,
      "Failed to delete car",
      "Error Message",
      JOptionPane.ERROR_MESSAGE
    );
  }

